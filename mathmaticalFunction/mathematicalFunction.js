// Math.abs used for absolute(making positive) value of a number)
console.log(Math.abs(-7)); 
// output: 7;

// Math.max used for choseing maximum value in the  Number.
console.log (Math.max(8,4,2,3,8,7));
// output: 8;

// Math.min  used for choseing maximum value in the  Number.
console.log (Math.min(8,4,2,3,8,7));
// output: 2;

// Math.pow used for given power 
console.log(Math.pow(2,5)); 
 // first value is a value but second ......value is power
// output: 32

// Math.sqrt used for square root of a number.
console.log(Math.sqrt(10));
// output: 3.1622776601683795

// Math.cbrt used for cube root of a number.
console.log(Math.cbrt(27));
// output: 3

// Math.log used for logarithm of a number.
console.log(Math.log10(10));
// output: 1

// Math.floor used for rounding down to the nearest integer.
console.log(Math.floor(12.7));
// output: 12

// Math.ceil used for rounding up to the nearest integer.
console.log(Math.ceil(5.8));
// output: 6

// Math.random used 
console.log(Math.random());